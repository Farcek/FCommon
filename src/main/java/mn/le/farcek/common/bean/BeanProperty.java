package mn.le.farcek.common.bean;

import mn.le.farcek.common.objects.FNamedObject;

/**
 *
 * @author Farcek
 */
public class BeanProperty implements FNamedObject{
    private String name;
    private Class type;

    public BeanProperty() {
    }
    

    public BeanProperty(String name, Class type) {
        this.name = name;
        this.type = type;
    }

    @Override
    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Class getType() {
        return type;
    }

    public void setType(Class type) {
        this.type = type;
    }
    
}
