package mn.le.farcek.common.utils;

import java.util.Random;

/**
 *
 * @author Farcek
 */
public class FRandomUtils {

    public static int randInt(int min, int max) {
        Random rand = new Random();
        int randomNum = rand.nextInt((max - min) + 1) + min;
        return randomNum;
    }
}
